    private void comboOptionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboOptionItemStateChanged
       if(comboOption.getSelectedItem().equals("Comment")){
           spnQntEmoji.setEnabled(false);
           fieldFrase.setEditable(true);
           jLabel3.setText("Phrase");
       }else if(comboOption.getSelectedItem().equals("Emoji")){
           spnQntEmoji.setEnabled(true);
           fieldFrase.setEditable(false);
       }else if(comboOption.getSelectedItem().equals("@")){
           spnQntEmoji.setEnabled(false);
           fieldFrase.setEditable(true);
           jLabel3.setText("@         ");
       }
    }//GEN-LAST:event_comboOptionItemStateChanged

