    private void btnReadyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadyActionPerformed
        
        //Elementos extraidos.
        String user = fieldUser.getText();
        String pass = fieldPass.getText();
        String phrase = fieldFrase.getText();
        String target = fieldAlvo.getText();
        String browser = fieldBrowser.getText();
        Integer qntCommentHours = sliderQuantidadeCommentHours.getValue();
        Integer qntHours = sliderQuantidadeHours.getValue();
        Integer numberTargetPhoto = Integer.valueOf(spnPhoto.getValue().toString());
        String objective = comboOption.getSelectedItem().toString();
        Integer qntEmojis = Integer.valueOf(spnQntEmoji.getValue().toString());
        
        //Setando driver antes da execução.  
        if(browser.equalsIgnoreCase("chrome")) {
            System.setProperty("webdriver.chrome.drivew","c:/autodrivers/chromedriver.exe");
            driver = new ChromeDriver();
        }else {
            System.setProperty("webdriver.gecko.driver","c:/autodrivers/geckodriver.exe");
            driver = new FirefoxDriver();
        }

        new LoginPO(driver)
                .login(user, pass)
                .search(target)
                .comment(phrase, qntCommentHours, numberTargetPhoto, qntHours, objective, qntEmojis);
    }//GEN-LAST:event_btnReadyActionPerformed

